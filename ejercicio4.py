area = float(input('Teclee los metros de la base  de su finca:' ))
altura = float(input('Teclee la altura de su finca:' ))
print ('Los metros cuadrados de su finca son:',(area*altura),('metros cuadrados'))
print ('El perímetro de su finca es',(area*2) + (altura*2))
